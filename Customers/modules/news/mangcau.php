<p style="font-family: Times New Roman;position: relative;top: 150px;font-size: 20px;">Mãng cầu xiêm (tên khoa học là Annona muricata L.) là một loại cây ăn trái thuộc họ Na. Trong dân gian, loại cây này còn có nhiều tên gọi khác như mãng cầu gai, na gai hoặc na xiêm.

Cây thân gỗ có chiều cao dao động từ 6 – 8 mét. Từ thân có thể mọc ra nhiều nhánh gọi là cành. Lá mọc so le, bề mặt nhẵn, hình trái xoăn. Hoa có 6 cánh úp vào nhau, 3 cánh ngoài màu xanh vàng và 3 cánh bên trong sắc vàng tươi. Quả mọng hình trứng, bên ngoài có nhiều gai. Thịt quả bên trong màu trắng và có nhiều hạt màu nâu đen.

mãng cầu xiêm
Mãng cầu xiêm vừa cung cấp nhiều chất dinh dưỡng vừa có tác dụng trị bệnh

 Mỗi bộ phận của cây như vỏ thân, quả, hoa, lá, rễ hay hạt đều có những công dụng tốt đối với sức khỏe. Trong đó quả mãng cầu xiêm là loại trái cây được nhiều người ưa thích và sử dụng phổ biến. 


