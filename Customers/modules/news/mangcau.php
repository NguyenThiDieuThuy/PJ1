+ Trị đau gan, tiểu tiện ra máu, bệnh ở đường tiết niệu

Dùng thịt của quả mãng cầu xiêm ép nước uống.

+ Điều trị đau nhức xương khớp

Hái 1 nắm lá rửa sạch, giã nát, đắp vào khu vực bị đau nhức ngày 2 – 3 lần

+ Trị viêm tấy, vết thương ngoài da

Sắc lá mãng cầu xiêm uống
Hoặc lấy thịt quả giã nát, đắp bên ngoài tổn thương
+ Ngăn ngừa và điều trị giun sán, ký sinh trùng:

Xay nát một nắm hạt mãng cầu gai, pha thêm nước đun sôi để nguội vào lọc uống

+ Ngăn ngừa cao huyết áp:

Dùng 20g lá mãng cầu xiêm tươi hoặc khô hãm uống như trà trong thời gian dài để giữ huyết áp luôn ở mức ổn định.

+ Trị tiêu chảy, nôn ói

Dùng hoa của cây mãng cầu xiêm nấu nước uống để bù nước, cầm tiêu chảy, giảm ói mửa.

+ Chữa chàm da

Rửa sạch một nắm lá tươi với nước muối, giã nát, đắp trực tiếp lên vùng da bị chàm mỗi ngày 1 – 2 lần.

+ Giảm cân:

Say nhuyễn quả mãng cầu xiêm làm sinh tố uống. Nên dùng nguyên chất, không thêm đường, sữa.

+ Trị mất ngủ:

Hái lá mãng cầu gai non pha trà uống hoặc nấu canh, luộc ăn thay rau để chữa mất ngủ

+ Mãng cầu xiêm chữa ung thư

Dùng lá tươi hoặc khô hãm với nước sôi uống
Hoặc lấy 10 lá sắc với 3 chén nước lấy 1 chén uống hàng ngày.
+ Chữa đau bụng kinh

Dùng 1 quả mãng cầu xiêm xanh sắc kỹ với 3,5 lít nước. Nấu đến khi thấy thịt quả mềm ra thì thêm 200g đường đỏ vào, quậy tan. Chia làm 2 lần ăn hết trong ngày.

+ Chữa viêm đường tiết niệu

Sắc 15 lá mãng cầu với 1 lít nước cho cạn còn 1 nửa. Uống 2 – 3 lần trong ngày cho hết.

+ Chữa bệnh trĩ

Ép quả mãng cầu xiêm chín lấy nước uống. Đều đặn dùng ngày 2 cốc giúp đi ngoài đều đặn, bớt đau và chảy máu khi đại tiện.

+ Chữa sưng bầm

Lấy rễ giá nát, ngâm với rượu trong khoảng 1 tuần. Khi dùng, thoa rượu tại chỗ kết hợp mát xa vài lần trong ngày để đánh tan vết bầm.

 Cách ăn quả mãng cầu xiêm
Quả mãng cầu xiêm vị chua ngọt, giúp thanh nhiệt, giải khát, làm đẹp da, tăng cường sức đề kháng cho cơ thể nên được rất nhiều người ưa chuộng. Nếu bạn chưa biết cách chế biến mãng cầu xiêm như thế nào cho ngon miệng có thể tham khảo một số gợi ý sau:

– Ăn trực tiếp:

Đem quả xả dưới vòi nước vài phút cho trôi sạch bụi bẩn, ký sinh trùng và chất bảo vệ thực vật nếu có
Dùng tay hoặc dao lột sạch lớp vỏ bên ngoài. Sau đó loại bỏ cuống cũng như phần lõi trắng bên trong
Cắt thịt quả thành nhiều miếng nhỏ, ăn ngay hoặc cho vào tủ lạnh để làm mát khoảng 1 tiếng trước khi dùng
– Làm sinh tố mãng cầu xiêm:

Chuẩn bị 300g thịt mãng cầu xiêm ( bỏ hạt ), 300ml sữa tươi, 80ml sữa đặc có đường, đá bào
Cho tất cả vào máy xay sinh tố xay cho nhuyễn mịn. Nếm thử nếu thấy chưa đủ độ ngọt thì cho thêm sữa đặc và đường vào tiếp tục xay vài giây nữa.
Cuối cùng bạn chỉ cần rót sinh tố mãng cầu ra ly và thưởng thức. Nếu xay nhiều một lúc, bạn có thể đổ sinh tố vào từng bịch nhỏ, cột chặt miệng, để vào ngăn đông tủ lạnh sẽ có ngay món kem mãng cầu để ăn dần.
