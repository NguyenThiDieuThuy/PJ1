		Dù gặp không ít khó khăn do dịch COVID-19 và thời tiết bất lợi nhưng các nhà vườn vẫn cố gắng tạo ra các sản phẩm mới lạ, độc đáo để phục vụ khách hàng dịp tết sắp tới.

Ông Trần Thanh Khang, Giám đốc Hợp tác xã Hoa kiểng Tân Quy Đông (TP Sa Đéc, Đồng Tháp), nói mỗi khi có thông tin về dịch COVID-19 bùng phát là thị trường hoa lại chững lại.

