		Chỉ cần gõ tìm giống cây sung Mỹ trên trang tìm kiếm, ngay lập tức xuất hiện hàng trăm trang trại, đơn vị giới thiệu tường tận, cung cấp đầy đủ thông tin về cây sung Mỹ.

Trong khi đó, mặt hàng sung tươi chín lại khá khan hiếm. Ông Tùng, một đầu mối cung cấp trái sung Mỹ tươi chín có địa chỉ tại Q.9, TP.HCM, cho biết: "Ít nhất hai tuần nữa mới có hàng, khách muốn mua phải "xếp hàng" đợi tới lượt, mức giá cũng cao ngất ngưởng từ 450.000 – 600.000 đồng/kg".

Săn lùng sung Mỹ thơm ngọt - Ảnh 2.
Sở dĩ bỗng nhiên quả sung Mỹ được săn lùng bởi sở thích "săn" hàng lạ của nhiều người. Bên cạnh đó, sung Mỹ có kích cỡ lớn, màu sắc hấp dẫn, bắt mắt nên gây tò mò cho nhiều người mua.

Chị Mai Ngọc (Q.Phú Nhuận, TP.HCM) người từng trồng sung Mỹ tại nhà cho biết, chị trồng giống cây này từ 2-3 năm trước đây. Giống cây này dễ sinh trưởng nhưng ra trái và chất lượng trái đòi hỏi kỹ thuật tỉ mỉ, cũng như sự khéo léo chăm sóc.

"Sung Mỹ có vị ngọt thanh, thơm và có chút dai nhẹ, nhiều trái chín mọng có lớp mật vàng sáng bên trong tạo cảm giác thơm ngon hơn hẳn sung Việt", chị Mai Ngọc cho hay.

Sung Mỹ dinh dưỡng cao

So với sung Việt, trái sung Mỹ có kích thước lớn hơn, vỏ mỏng hơn. Bên trong ruột quả sung có mật màu vàng nhạt vị ngọt rất hấp dẫn. Khi chín, loại sung này có màu vàng sẫm cho đến tím thẫm. Sung Mỹ sấy khô được đánh giá cao về hương vị cũng như giá trị dinh dưỡng. Quả này giàu chất xơ, sắt, kali, canxi, magie, phốt pho và vitamin.

Theo tìm hiểu, quả sung Mỹ có giá trị dinh dưỡng cao, nhiều lợi ích đối với sức khỏe như chống táo bón, ngăn ngừa bệnh động mạch vành, hỗ trợ điều trị ung thư đại tràng và điều trị viêm phế quản, tăng cường canxi, ngăn ngừa thoái hóa thị lực...

Sung Mỹ trồng trên đất Việt

Hiện nay, nhờ công nghệ hiện đại nên dù là giống cây ôn đới nhưng được lai tạo, sung Mỹ đã được lựa chọn và trồng trên khắp các châu lục và ở Việt Nam. Sung Mỹ là giống ưa khí hậu ấm áp, thuộc khu vực cận nhiệt đới. Đây là loại cây ưa ánh sáng mặt trời, nên trồng ở nơi có ánh sáng ít nhất 8 tiếng/ngày.

Theo các chuyên gia, sung Mỹ thích hợp gieo trồng đầu mùa mưa, độ ẩm cao. Sung mỹ không thích hợp với những nơi có ánh sáng gay gắt, vì vậy nên để cây ở những nơi có cường độ ánh sáng thấp.

Chăm sóc cung cấp nước, dinh dưỡng cho cây đều đặn cho tới khi ra hoa, mất từ 6-8 tháng cây sung Mỹ sẽ ra trái, để kích thích cây ra trái nên ngưng tưới nước từ 15-20 ngày đồng thời vặt bỏ lá. Sau thu hoạch lại tiếp tục quy trình như cũ.
