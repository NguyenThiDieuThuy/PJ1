<p style="font-family: Times New Roman;position: relative;top: 150px;font-size: 20px;">Dứa (có tên khoa học là Ananas comosus) là một loại trái cây nhiệt đới cực kỳ ngon và tốt cho sức khỏe.

Nó có nguồn gốc ở Nam Mỹ, nơi các nhà thám hiểm châu Âu đầu tiên đặt tên cho nó sau khi giống với một trái thông (pine), do đó trong tiếng Anh thì dứa được gọi là pineapple.<br>

Loại trái cây phổ biến này có chứa các chất dinh dưỡng, chất chống oxy hóa và các hợp chất hữu ích khác, chẳng hạn như các enzyme có thể chống viêm và nhiều loại bệnh khác.

Dứa và các hợp chất của nó đã được chứng minh là mang lại nhiều lợi ích cho sức khỏe, bao gồm hỗ trợ tiêu hóa, tăng cường khả năng miễn dịch và tăng tốc độ phục hồi sau phẫu thuật. 

Ở bài viết dưới đây, samya.vn xin giới thiệu tới bạn đọc 8 lợi ích sức khỏe ấn tượng của dứa.<br>

1. Dứa có chưa nhiều chất dinh dưỡng
Dứa có lượng calo thấp nhưng có thành phần dinh dưỡng vô cùng ấn tượng.

Không chỉ vậy, dứa cũng chứa một lượng vitamin A và K, phốt pho, kẽm và canxi trong thành phần dinh dưỡng của nó.

Dứa cũng đặc biệt giàu vitamin C và mangan, cung cấp tương ứng 131% và 76% chất dinh dưỡng khuyến nghị hàng ngày.

Vitamin C rất cần thiết cho sự tăng trưởng và phát triển, hệ thống miễn dịch khỏe mạnh và hỗ trợ hấp thu sắt từ chế độ ăn uống. Trong khi đó, mangan là một khoáng chất tự nhiên giúp tăng trưởng, duy trì sự trao đổi chất lành mạnh và có đặc tính chống oxy hóa.

<br>TÓM LƯỢC: Dứa được đóng gói với nhiều loại vitamin và khoáng chất. Chúng đặc biệt giàu vitamin C và mangan.<br>

2. Chứa chất chống oxy hóa chống bệnh
Không chỉ là dứa giàu chất dinh dưỡng, chúng còn được mệnh danh là thực phẩm có chứa chống oxy hóa lành mạnh.

Chất chống oxy hóa là các phân tử giúp cơ thể bạn chống lại stress oxy hóa (hay còn được gọi là Ứng kích ôxi hóa)

Stress oxy hóa là một trạng thái trong đó có quá nhiều gốc tự do trong cơ thể. Các gốc tự do này tương tác với các tế bào của cơ thể và gây ra thiệt hại có liên quan đến viêm mãn tính, hệ thống miễn dịch suy yếu và nhiều bệnh có hại.

Dứa đặc biệt giàu chất chống oxy hóa được gọi là flavonoid và axit phenolic.

Hơn nữa, nhiều chất chống oxy hóa trong dứa bị ràng buộc. Điều này cho phép các chất chống oxy hóa tồn tại trong điều kiện khắc nghiệt hơn trong cơ thể và tạo ra hiệu quả lâu dài hơn.
<br>
TÓM LƯỢC: Dứa là một loại quả có nguồn chất chống oxy hóa tốt, có thể làm giảm nguy cơ mắc các bệnh mãn tính như bệnh tim, tiểu đường và một số bệnh ung thư. Nhiều chất chống oxy hóa trong dứa bị nén lại, vì vậy chúng có thể có tác dụng lâu dài hơn.<br>

3. Enzyme của nó có thể dễ dàng tiêu hóa
Dứa chứa một nhóm các enzyme tiêu hóa được gọi là bromelain.

Chúng hoạt động như các protease, phá vỡ các phân tử protein thành các khối xây dựng của chúng, chẳng hạn như axit amin và các peptide nhỏ.

Một khi các phân tử protein bị phá vỡ, chúng dễ dàng được hấp thụ qua ruột non hơn. Điều này có thể đặc biệt hữu ích cho những người bị suy tụy, một tình trạng trong đó tuyến tụy không thể tạo ra đủ các enzyme tiêu hóa.

Ví dụ, một nghiên cứu cho thấy những người tham gia bị suy tụy có khả năng tiêu hóa tốt hơn sau khi uống bổ sung enzyme tiêu hóa có chứa bromelain, so với việc bổ sung enzyme tiêu hóa mà không có bromelain.

Bromelain cũng được sử dụng rộng rãi như một chất làm mềm thịt thương mại do khả năng phá vỡ các protein thịt cứng.<br>

TÓM LƯỢC: Dứa chứa bromelain, một nhóm các enzyme tiêu hóa phá vỡ protein. Điều này có thể hỗ trợ tiêu hóa, đặc biệt là ở những người bị suy tụy.<br>

4. Có thể giúp giảm nguy cơ ung thư
Ung thư là một bệnh mãn tính đặc trưng bởi sự tăng trưởng tế bào không kiểm soát được.

Sự tiến triển của nó thường liên quan đến stress oxy hóa và viêm mãn tính.

Một số nghiên cứu đã chỉ ra rằng dứa và các hợp chất của nó có thể làm giảm nguy cơ mắc bệnh ung thư. Điều này là do họ có thể giảm thiểu căng thẳng oxy hóa và giảm viêm.

Một trong những hợp chất này là nhóm các enzyme tiêu hóa được gọi là bromelain. Các nghiên cứu về ống nghiệm đã chỉ ra rằng bromelain cũng có thể giúp chống ung thư.

Trên thực tế hiện đã có hai nghiên cứu ống nghiệm cho thấy bromelain đã ngăn chặn sự phát triển của tế bào ung thư vú và kích thích sự chết của tế bào.

Các nghiên cứu ống nghiệm khác cho thấy bromelain ức chế ung thư ở da, ống mật, hệ thống dạ dày và ruột kết, trong số các lĩnh vực khác.

Các nghiên cứu trên ống nghiệm và động vật đã phát hiện ra rằng bromelain có thể kích thích hệ thống miễn dịch để tạo ra các phân tử làm cho các tế bào bạch cầu hiệu quả hơn trong việc ngăn chặn sự phát triển của tế bào ung thư và loại bỏ các tế bào ung thư.

Điều đó nói rằng, dứa chứa bromelain ít hơn nhiều so với bổ sung. Cần nhiều nghiên cứu dựa trên con người trước khi đưa ra bất kỳ kết luận nào.<br>

TÓM LƯỢC: Dứa chứa các hợp chất làm giảm căng thẳng oxy hóa và viêm, cả hai đều có liên quan đến ung thư. Một trong những hợp chất này là enzyme bromelain, có thể kích thích sự chết tế bào ở một số tế bào ung thư và hỗ trợ chức năng tế bào bạch cầu.



